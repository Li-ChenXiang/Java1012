package hd;

public class HardDisk {
	int amount;
	float weight;
	HardDisk(){
	}
	public int getAmount(){
		return amount;
	}
	
	public float getWeight(){
		return weight;
	}
	
	public void setAmount(int m){
		this.amount=m;
	}
	public void setWeight(float w){
		this.weight=w;
	}

}
